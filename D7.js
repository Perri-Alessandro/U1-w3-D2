/* ESERCIZIO 1
  Scrivi una funzione per concatenare due stringhe ricevute come parametri, selezionando solamente i primi 2 caratteri della
  prima e gli ultimi 3 della seconda. Converti la stringa risultante in maiuscolo e mostrala con un console.log().
*/

const convert = function (str1, str2) {
  const nuovaStr = str1.slice(0, 2).concat(str2.slice(-3)).toUpperCase();
  console.log(
    "2 STRINGHE CONCATENATE, SOLO PRIMI 2 CARATTERI DI PRIMA ED ULTIMI 3 DI SECONDA E DIVENTANO MAIUSCOLE:",
    nuovaStr
  );
};
convert("ciao", "allora");

/* ESERCIZIO 2 (for)
  Scrivi una funzione che torni un array di 10 elementi; ognuno di essi deve essere un valore random compreso tra 0 e 100 (incluso).
*/

const array = [];
const numericArray = function () {
  for (let i = 0; i < 10; i++) {
    const randomN = Math.floor(Math.random() * 101);
    array.push(randomN);
  }
  console.log("CREATO ARRAY DI 10 NUMERI, VALORE RANDOM DA 1 A 100:", array);
  return array;
};
numericArray();

